# 1. Slicing (Срезы)
b = "Hello, World!"
print(b[2:5])

# 2. Методы: upper() и lower()
print(b.upper())

# 3. Удаление пробелов (strip)
a = " Hello, World! "
print(a.strip())

# 4. Замена (replace)
print(b.replace("H", "J"))

# 5. Форматирование строк (f-strings)
age = 18
txt = f"My name is John, I am {age}"
print(txt)