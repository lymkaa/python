# 1. int() - целые числа
x = int(1)   # x will be 1
y = int(2.8) # y will be 2

# 2. float() - дробные числа
a = float(1)     # a will be 1.0
b = float("4.2") # b will be 4.2

# 3. str() - строки
s1 = str("s1") # s1 will be "s1"
s2 = str(2)    # s2 will be "2"

# 4. Преобразование float в int
print(int(3.999))

# 5. Преобразование строки в float
print(float("10"))